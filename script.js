document.addEventListener('DOMContentLoaded', () => {

    // --- 0. Language Selector ---
    const langSelect = document.getElementById('lang-select');
    const updateLanguage = (lang) => {
        const t = translations[lang];
        if (!t) return;

        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (t[key]) {
                el.innerText = t[key];
            }
        });
    };

    langSelect.addEventListener('change', (e) => {
        updateLanguage(e.target.value);
    });

    updateLanguage(langSelect.value);

    // --- 1. Countdown Timer ---
    const weddingDate = new Date('Jul 5, 2026 07:15:00').getTime();

    const updateCountdown = () => {
        const now = new Date().getTime();
        const distance = weddingDate - now;

        if (distance < 0) {
            document.getElementById('countdown').innerHTML = "<h3 style='color: var(--accent-gold);'>The Celebration Has Begun!</h3>";
            return;
        }

        const days = Math.floor(distance / (1000 * 60 * 60 * 24));
        const hours = Math.floor((distance % (1000 * 60 * 60 * 24)) / (1000 * 60 * 60));
        const minutes = Math.floor((distance % (1000 * 60 * 60)) / (1000 * 60));
        const seconds = Math.floor((distance % (1000 * 60)) / 1000);

        document.getElementById('days').innerText = String(days).padStart(2, '0');
        document.getElementById('hours').innerText = String(hours).padStart(2, '0');
        document.getElementById('minutes').innerText = String(minutes).padStart(2, '0');
        document.getElementById('seconds').innerText = String(seconds).padStart(2, '0');
    };

    setInterval(updateCountdown, 1000);
    updateCountdown();

    // --- 2. Scroll Animations (Intersection Observer) ---
    const observerOptions = {
        threshold: 0.1,
        rootMargin: "0px 0px -50px 0px"
    };

    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('show');
                observer.unobserve(entry.target);
            }
        });
    }, observerOptions);

    const hiddenElements = document.querySelectorAll('.hidden');
    hiddenElements.forEach(el => observer.observe(el));

    // --- 3. Audio Toggle ---
    const audioBtn = document.getElementById('audio-btn');
    const bgMusic = document.getElementById('bg-music');
    let isPlaying = false;
    let autoPauseTimer = null;

    bgMusic.volume = 0.4;

    function startAutoPause() {
        clearTimeout(autoPauseTimer);
        autoPauseTimer = setTimeout(() => {
            bgMusic.pause();
            audioBtn.classList.remove('playing');
            audioBtn.innerHTML = '<i class="fas fa-music"></i>';
            isPlaying = false;
        }, 60000);
    }

    audioBtn.addEventListener('click', () => {
        if (isPlaying) {
            bgMusic.pause();
            audioBtn.classList.remove('playing');
            audioBtn.innerHTML = '<i class="fas fa-music"></i>';
            clearTimeout(autoPauseTimer);
        } else {
            bgMusic.play().catch(error => console.log("Audio play failed:", error));
            audioBtn.classList.add('playing');
            audioBtn.innerHTML = '<i class="fas fa-pause"></i>';
            startAutoPause();
        }
        isPlaying = !isPlaying;
    });

    let wasPlayingBeforeHide = false;

    const handleHide = () => {
        if (isPlaying) {
            wasPlayingBeforeHide = true;
            bgMusic.pause();
            clearTimeout(autoPauseTimer);
        }
    };

    const handleShow = () => {
        if (wasPlayingBeforeHide) {
            wasPlayingBeforeHide = false;
            bgMusic.play().catch(() => {});
            startAutoPause();
        }
    };

    document.addEventListener('visibilitychange', () => {
        document.hidden ? handleHide() : handleShow();
    });

    window.addEventListener('pagehide', handleHide);
    window.addEventListener('pageshow', handleShow);
    document.addEventListener('freeze', handleHide);
    document.addEventListener('resume', handleShow);

    document.querySelectorAll('.btn-directions, a[href*="maps"]').forEach(link => {
        link.addEventListener('click', () => {
            if (isPlaying) {
                bgMusic.pause();
                audioBtn.classList.remove('playing');
                audioBtn.innerHTML = '<i class="fas fa-music"></i>';
                isPlaying = false;
                clearTimeout(autoPauseTimer);
            }
        });
    });

    // --- Curtain Tap Invitation Logic ---
    const btnOpen = document.getElementById('btn-open');
    let curtainOpened = false;

    if (btnOpen) {
        btnOpen.addEventListener('click', () => {
            if (curtainOpened) return;
            curtainOpened = true;

            const cCta = document.getElementById('c-cta');
            cCta.style.opacity = '0';
            cCta.style.transform = 'scale(0.95)';

            setTimeout(() => {
                document.getElementById('cpl').classList.add('open');
                document.getElementById('cpr').classList.add('open');

                if (!isPlaying) {
                    bgMusic.play().catch(e => console.log("Audio play failed:", e));
                    audioBtn.classList.add('playing');
                    audioBtn.innerHTML = '<i class="fas fa-pause"></i>';
                    isPlaying = true;
                    startAutoPause();
                }
            }, 300);

            setTimeout(() => {
                document.getElementById('curtain').classList.add('gone');
            }, 1900);
        });
    }

    // --- Falling Petals ---
    const particlesContainer = document.getElementById('particles');
    const createParticle = () => {
        const particle = document.createElement('div');
        particle.classList.add('petal');

        const size = Math.random() * 15 + 10;
        particle.style.width = size + 'px';
        particle.style.height = size + 'px';
        particle.style.left = Math.random() * 100 + 'vw';

        const duration = Math.random() * 6 + 6;
        const sway = Math.random() * 150 - 75;
        const rotations = Math.random() * 720;

        particle.animate([
            { transform: 'translateY(-10vh) translateX(0) rotate(0deg)', opacity: 0 },
            { opacity: 0.8, offset: 0.1 },
            { opacity: 0.8, offset: 0.9 },
            { transform: `translateY(110vh) translateX(${sway}px) rotate(${rotations}deg)`, opacity: 0 }
        ], {
            duration: duration * 1000,
            easing: 'ease-in-out',
            fill: 'forwards'
        });

        particlesContainer.appendChild(particle);

        setTimeout(() => {
            particle.remove();
        }, duration * 1000);
    };

    setInterval(createParticle, 400);

    // --- Family Invites Sliders ---
    const sliders = {
        bride: { index: 1, timer: null, slideClass: 'slide-bride', dotClass: 'dot-bride' },
        groom: { index: 1, timer: null, slideClass: 'slide-groom', dotClass: 'dot-groom' }
    };

    const showSlide = (group, n) => {
        const s = sliders[group];
        const slides = document.getElementsByClassName(s.slideClass);
        const dots = document.getElementsByClassName(s.dotClass);
        if (!slides.length) return;

        if (n > slides.length) s.index = 1;
        else if (n < 1) s.index = slides.length;
        else s.index = n;

        for (let i = 0; i < slides.length; i++) slides[i].style.display = 'none';
        for (let i = 0; i < dots.length; i++) dots[i].classList.remove('active');

        slides[s.index - 1].style.display = 'block';
        if (dots[s.index - 1]) dots[s.index - 1].classList.add('active');
    };

    const startTimer = (group) => {
        const s = sliders[group];
        clearInterval(s.timer);
        s.timer = setInterval(() => showSlide(group, s.index + 1), 3500);
    };

    window.changeSlide = (group, n) => {
        showSlide(group, sliders[group].index + n);
        startTimer(group);
    };

    window.currentSlide = (group, n) => {
        showSlide(group, n);
        startTimer(group);
    };

    showSlide('bride', 1); startTimer('bride');
    showSlide('groom', 1); startTimer('groom');

    // --- Fade in event card background images on scroll ---
    const bgObserver = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('bg-loaded');
                bgObserver.unobserve(entry.target);
            }
        });
    }, { threshold: 0.2 });

    document.querySelectorAll('.tl-ev-bg').forEach(bg => bgObserver.observe(bg));
});
